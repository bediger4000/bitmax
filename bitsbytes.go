package main

import "unsafe"

// bits2 finds the number of 1-bits in the argument
// integer by looking up the numerical value of each
// byte in the argument integer.
// For a 64-bit integer, it does 8 indexes into a
// pre-calculated array of 1-bits-per-byte,
// and 8, 8-bit shifts.
func bits2(n int) int {

	count := 0

	bytes := int(unsafe.Sizeof(n))

	for i := 0; i < bytes; i++ {
		count += bitsPerByte[n&0xff]
		n >>= 8
	}
	return count
}

